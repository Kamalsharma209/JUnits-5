package org.example;

public class MathUtils {
    public int  add(int a, int b) {
        return a + b;
    }
    public int mul(int a , int b){
        return a * b;
    }
    public double AreaOfCircle(double r){
        return Math.PI * r *r;

    }
    public double circumference(double r){
        return Math.PI * 2* r;
    }
    public int div (int a , int b){
        return a / b;
    }
}
